#include <stdio.h>

int main()
{
    double n, a, a10, a20, a40, a60, a100;

    a = 0;
    a10 = 100000 * 0.1;
    a20 = a10 + (200000 - 100000) * 0.075;
    a40 = a20 + (400000 - 100000) * 0.05;
    a60 = a40 + (600000 - 400000) * 0.03;
    a100 = a60 + (1000000 - 600000) * 0.015;

    scanf("%lf", &n);

    if (n <= 100000)
    {
        a = n * 0.1;
    }
    else if (n <= 200000)
    {
        a = a10 + (n - 100000) * 0.075;
    }
    else if (n <= 400000)
    {
        a = a20 + (n - 200000) * 0.05;
    }
    else if (n <= 600000)
    {
        a = a40 + (n - 400000) * 0.03;
    }
    else if (n <= 1000000)
    {
        a = a60 + (n - 600000) * 0.015;
    }
    else
    {
        a = a100 + (n - 1000000) * 0.01;
    }
    printf("%.2f\n", a);
}